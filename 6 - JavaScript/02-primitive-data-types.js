// Primitive Data Types

// Strings: Textual data enclosed in '' or ""
let firstName = "John"
console.log("First Name:", firstName);

// typeof operator: determine the the type of a variable (built-in operator)
// operand: variable or expression whose type you want to check
// Syntax: typeof operand
console.log("Type of first name:", typeof firstName);

// Number: Represents numeric value (eg: 30, -30, 3.14)
let age = 30;
console.log("Age:", age);
console.log("Type of age:", typeof age);

// Boolean: Either true or false
let isStudent = false;
console.log("Is Student?", isStudent);
console.log("Type of isStudent:", typeof isStudent);

// Null: Intentional absence of value
// Use Case: Resetting or Clearing Values
let car = null;
console.log("Car:", car);
console.log("Type of Car:", typeof car);

// undefined: unintentional absence of value
let city;
console.log("City:", city);
console.log("Type of City:", typeof city);

